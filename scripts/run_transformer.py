class RepoAnalyzer:
    """Analyzes a repository and extracts metadata for agent scaffolding."""

    def __init__(self, target: Path):
        self.target = target.resolve()
        self.name = self.target.name
        self.files: list[Path] = []
        self.languages: dict[str, int] = {}
        self.frameworks: list[str] = []
        self.build_system: str = "unknown"
        self.entry_points: list[str] = []
        self.test_framework: str = "unknown"
        self.test_directory: str = ""
        self.source_dirs: list[str] = []
        self.key_files: dict[str, str] = {}
        self.commands: dict[str, str] = {}
        self.description: str = ""
        self.conventions: dict[str, str] = {}
        self.env_vars: list[str] = []
        self.ci_platform: str = ""
        self.ci_config: str = ""

    def analyze(self) -> dict[str, Any]:
        """Run full repository analysis and return structured metadata."""
        print(f"🔍 Analyzing repository: {self.target}")

        self._scan_files()
        self._detect_languages()
        self._detect_config_and_build()
        self._detect_frameworks()
        self._detect_entry_points()
        self._detect_tests()
        self._detect_source_dirs()
        self._detect_key_files()
        self._detect_conventions()
        self._detect_ci()
        self._detect_env_vars()
        self._read_description()
        self._infer_commands()

        return self._to_dict()

    def _scan_files(self) -> None:
        """Walk the repository and collect all file paths."""
        for root, dirs, files in os.walk(self.target):
            # Prune ignored directories
            dirs[:] = [d for d in dirs if d not in IGNORE_DIRS]
            for f in files:
                full = Path(root) / f
                self.files.append(full)

        print(f"  📁 Found {len(self.files)} files")

    def _detect_languages(self) -> None:
        """Count files per language based on extensions."""
        for f in self.files:
            ext = f.suffix.lower()
            for lang, exts in LANGUAGE_EXTENSIONS.items():
                if ext in exts:
                    self.languages[lang] = self.languages.get(lang, 0) + 1
                    break

        # Sort by count descending
        self.languages = dict(
            sorted(self.languages.items(), key=lambda x: x[1], reverse=True)
        )
        if self.languages:
            print(f"  🌐 Languages: {', '.join(self.languages.keys())}")

    def _detect_config_and_build(self) -> None:
        """Detect build system from config files."""
        for config_file, info in CONFIG_FILES.items():
            if (self.target / config_file).exists():
                if info["build"] != "make" or self.build_system == "unknown":
                    self.build_system = info["build"]
                break

        print(f"  🔧 Build system: {self.build_system}")

    def _detect_frameworks(self) -> None:
        """Detect frameworks from dependency files."""
        # Check package.json
        pkg_json = self.target / "package.json"
        if pkg_json.exists():
            try:
                pkg = json.loads(pkg_json.read_text(encoding="utf-8"))
                all_deps = {
                    **pkg.get("dependencies", {}),
                    **pkg.get("devDependencies", {}),
                }
                for framework, indicators in FRAMEWORK_INDICATORS.items():
                    if any(ind in all_deps for ind in indicators):
                        self.frameworks.append(framework)
            except (json.JSONDecodeError, OSError):
                pass

        # Check requirements.txt / pyproject.toml
        for req_file in ["requirements.txt", "pyproject.toml", "Pipfile"]:
            req_path = self.target / req_file
            if req_path.exists():
                try:
                    content = req_path.read_text(encoding="utf-8").lower()
                    for framework, indicators in FRAMEWORK_INDICATORS.items():
                        if any(ind.lower() in content for ind in indicators):
                            if framework not in self.frameworks:
                                self.frameworks.append(framework)
                except OSError:
                    pass

        # Check go.mod
        go_mod = self.target / "go.mod"
        if go_mod.exists():
            try:
                content = go_mod.read_text(encoding="utf-8")
                for framework, indicators in FRAMEWORK_INDICATORS.items():
                    if any(ind in content for ind in indicators):
                        if framework not in self.frameworks:
                            self.frameworks.append(framework)
            except OSError:
                pass

        # Check pom.xml / build.gradle
        for build_file in ["pom.xml", "build.gradle", "build.gradle.kts"]:
            build_path = self.target / build_file
            if build_path.exists():
                try:
                    content = build_path.read_text(encoding="utf-8")
                    for framework, indicators in FRAMEWORK_INDICATORS.items():
                        if any(ind in content for ind in indicators):
                            if framework not in self.frameworks:
                                self.frameworks.append(framework)
                except OSError:
                    pass

        if self.frameworks:
            print(f"  📦 Frameworks: {', '.join(self.frameworks)}")

    def _detect_entry_points(self) -> None:
        """Find common entry point files."""
        entry_patterns = [
            "main.*", "app.*", "index.*", "server.*", "manage.py",
            "cli.*", "cmd/**/main.*", "src/main.*", "src/index.*",
            "src/app.*",
        ]
        for f in self.files:
            rel = f.relative_to(self.target)
            name = f.stem.lower()
            if name in ("main", "app", "index", "server", "manage", "cli"):
                self.entry_points.append(str(rel))

        if self.entry_points:
            print(f"  🚀 Entry points: {', '.join(self.entry_points[:5])}")

    def _detect_tests(self) -> None:
        """Find test directories and test framework."""
        for d in TEST_DIRECTORIES:
            test_dir = self.target / d
            if test_dir.is_dir():
                self.test_directory = d
                break

        # Detect test framework
        if (self.target / "pytest.ini").exists() or (self.target / "conftest.py").exists():
            self.test_framework = "pytest"
        elif (self.target / "jest.config.js").exists() or (self.target / "jest.config.ts").exists():
            self.test_framework = "jest"
        elif (self.target / "vitest.config.ts").exists():
            self.test_framework = "vitest"
        elif (self.target / ".rspec").exists():
            self.test_framework = "rspec"
        else:
            # Check package.json for test framework
            pkg_json = self.target / "package.json"
            if pkg_json.exists():
                try:
                    pkg = json.loads(pkg_json.read_text(encoding="utf-8"))
                    dev_deps = pkg.get("devDependencies", {})
                    for fw_name in ["jest", "mocha", "vitest", "ava", "tap"]:
                        if fw_name in dev_deps:
                            self.test_framework = fw_name
                            break
                except (json.JSONDecodeError, OSError):
                    pass

            # Check pyproject.toml
            pyproject = self.target / "pyproject.toml"
            if pyproject.exists() and self.test_framework == "unknown":
                try:
                    content = pyproject.read_text(encoding="utf-8")
                    if "pytest" in content:
                        self.test_framework = "pytest"
                except OSError:
                    pass

        if self.test_directory:
            print(f"  🧪 Tests: {self.test_directory}/ ({self.test_framework})")

    def _detect_source_dirs(self) -> None:
        """Identify source directories."""
        common_src_dirs = ["src", "lib", "app", "pkg", "internal", "cmd", "api", "core"]
        for d in common_src_dirs:
            if (self.target / d).is_dir():
                self.source_dirs.append(d)

    def _detect_key_files(self) -> None:
        """Identify important files and describe them."""
        key_file_patterns = {
            "README.md": "Project documentation and overview",
            "package.json": "Node.js dependencies and scripts",
            "tsconfig.json": "TypeScript compiler configuration",
            "pyproject.toml": "Python project metadata and dependencies",
            "requirements.txt": "Python dependencies",
            "go.mod": "Go module dependencies",
            "Cargo.toml": "Rust crate metadata and dependencies",
            "pom.xml": "Maven project configuration",
            "build.gradle": "Gradle build configuration",
            "Makefile": "Build automation targets",
            "Dockerfile": "Container image definition",
            "docker-compose.yml": "Multi-container Docker configuration",
            "docker-compose.yaml": "Multi-container Docker configuration",
            ".env.example": "Environment variable template",
            ".env.sample": "Environment variable template",
        }

        for filename, desc in key_file_patterns.items():
            if (self.target / filename).exists():
                self.key_files[filename] = desc

        # Add entry points
        for ep in self.entry_points[:5]:
            if ep not in self.key_files:
                self.key_files[ep] = "Application entry point"

    def _detect_conventions(self) -> None:
        """Detect coding conventions."""
        # Naming convention (heuristic from file names)
        snake_count = sum(1 for f in self.files if "_" in f.stem and f.stem[0].islower())
        camel_count = sum(
            1 for f in self.files
            if re.search(r"[a-z][A-Z]", f.stem) and not f.stem.startswith("_")
        )
        pascal_count = sum(1 for f in self.files if f.stem[0:1].isupper() and f.suffix in (".java", ".cs", ".kt"))

        if pascal_count > camel_count and pascal_count > snake_count:
            self.conventions["naming"] = "PascalCase"
        elif camel_count > snake_count:
            self.conventions["naming"] = "camelCase"
        else:
            self.conventions["naming"] = "snake_case"

        # Project structure
        if any((self.target / d).is_dir() for d in ["packages", "apps", "libs", "modules"]):
            self.conventions["structure"] = "monorepo"
        elif len(self.source_dirs) > 2:
            self.conventions["structure"] = "multi-module"
        else:
            self.conventions["structure"] = "single-package"

    def _detect_ci(self) -> None:
        """Detect CI/CD platform."""
        ci_files = {
            ".github/workflows": "GitHub Actions",
            ".gitlab-ci.yml": "GitLab CI",
            "Jenkinsfile": "Jenkins",
            ".circleci/config.yml": "CircleCI",
            ".travis.yml": "Travis CI",
            "azure-pipelines.yml": "Azure Pipelines",
            "bitbucket-pipelines.yml": "Bitbucket Pipelines",
        }
        for path, platform in ci_files.items():
            full = self.target / path
            if full.exists():
                self.ci_platform = platform
                self.ci_config = path
                break

    def _detect_env_vars(self) -> None:
        """Detect environment variables from .env files."""
        for env_file in [".env.example", ".env.sample", ".env.template"]:
            env_path = self.target / env_file
            if env_path.exists():
                try:
                    content = env_path.read_text(encoding="utf-8")
                    for line in content.splitlines():
                        line = line.strip()
                        if line and not line.startswith("#") and "=" in line:
                            var_name = line.split("=", 1)[0].strip()
                            self.env_vars.append(var_name)
                except OSError:
                    pass
                break

    def _read_description(self) -> None:
        """Extract project description from README."""
        readme = self.target / "README.md"
        if readme.exists():
            try:
                content = readme.read_text(encoding="utf-8")
                lines = content.splitlines()
                # Skip the title, find first paragraph
                in_paragraph = False
                paragraph_lines = []
                for line in lines[1:]:
                    stripped = line.strip()
                    if not stripped and in_paragraph:
                        break
                    if stripped and not stripped.startswith("#") and not stripped.startswith("!") and not stripped.startswith("["):
                        in_paragraph = True
                        paragraph_lines.append(stripped)

                if paragraph_lines:
                    self.description = " ".join(paragraph_lines)[:500]
            except OSError:
                pass

        if not self.description:
            self.description = f"A {', '.join(list(self.languages.keys())[:2]) or 'software'} project"

