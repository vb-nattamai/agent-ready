    def generate_tool_templates(self) -> None:
        """Generate tool template files for each detected language."""
        lang_to_template = {
            "Python": "tool.python.template.py",
            "TypeScript": "tool.typescript.template.ts",
            "JavaScript": "tool.typescript.template.ts",
            "Java": "tool.java.template.java",
            "Go": "tool.go.template.go",
        }

        lang_to_output = {
            "Python": "tools/example_tool.py",
            "TypeScript": "tools/example_tool.ts",
            "JavaScript": "tools/example_tool.ts",
            "Java": "tools/ExampleTool.java",
            "Go": "tools/example_tool.go",
        }

        generated_outputs = set()
        for lang in self.meta["primary_languages"]:
            template_file = lang_to_template.get(lang)
            output_file = lang_to_output.get(lang)
            if template_file and output_file and output_file not in generated_outputs:
                template_path = TEMPLATES_DIR / template_file
                if template_path.exists():
                    content = template_path.read_text(encoding="utf-8")
                    self._write_file(output_file, content)
                    generated_outputs.add(output_file)


