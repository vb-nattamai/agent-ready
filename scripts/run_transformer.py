def main() -> None:
    parser = argparse.ArgumentParser(
        description="Transform any repository into an AI-agent-ready codebase.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python run_transformer.py --target /path/to/repo
  python run_transformer.py --target /path/to/repo --only agents
  python run_transformer.py --target /path/to/repo --dry-run
  python run_transformer.py --target . --verbose
        """,
    )
    parser.add_argument(
        "--target", "-t",
        type=str,
        required=True,
        help="Path to the target repository to transform",
    )
    parser.add_argument(
        "--only",
        type=str,
        choices=["agents", "context", "tools", "mcp"],
        default=None,
        help="Only generate a specific category of files",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would be generated without writing files",
    )
    parser.add_argument(
        "--verbose", "-v",
        action="store_true",
        help="Enable verbose output",
    )

    args = parser.parse_args()
    target = Path(args.target).resolve()

    if not target.is_dir():
        print(f"❌ Target directory does not exist: {target}")
        sys.exit(1)

    print()
    print("╔══════════════════════════════════════════════╗")
    print("║   🤖 Repo-to-Agentic Transformer v1.0.0     ║")
    print("╚══════════════════════════════════════════════╝")
    print()

    # Phase 1: Analysis
    analyzer = RepoAnalyzer(target)
    metadata = analyzer.analyze()

    print()

    # Phase 2: Generation
    generator = AgenticGenerator(target, metadata, dry_run=args.dry_run)

    if args.only:
        generated = generator.generate_only(args.only)
    else:
        generated = generator.generate_all()

    # Summary
    print()
    print("─" * 46)
    mode = "[DRY RUN] " if args.dry_run else ""
    print(f"✅ {mode}Transformation Complete")
    print("─" * 46)
    print(f"  Project:    {metadata['project_name']}")
    print(f"  Languages:  {', '.join(metadata['primary_languages']) or 'Unknown'}")
    print(f"  Frameworks: {', '.join(metadata['frameworks']) or 'None detected'}")
    print(f"  Build:      {metadata['build_system']}")
    print()
    print("  Generated Files:")
    for f in generated:
        print(f"    ✓ {f}")
    print()
    print("  No existing files were modified.")
    print()


if __name__ == "__main__":
    main()
